# In Python, there are many data types. Let's see some of them.

#1. int - integer number(Positive, Negative, Zero)
age = 35
print(type(age))
age1 = 0
print(type(age1))
age2 = -35
print(type(age2)) 

#Float - float number(Decimal number)
age = 35.5
print(type(age))
age1 = 0.0
print(type(age1))
age2 = -35.5
print(type(age2)) 

#str - String(Sequence of characters)
name = "atanu"
print(type(name))

#Boolean - True or False
result = True
print(type(result))
result1 = False
print(type(result1)) 

#None Type
name = None
print(type(name))

#List (Array)
numbers = [1, 2, 3, 4, 5] 
print(type(numbers))

#Tuple
numbers = (1, 2, 3, 4, "hello")
print(type(numbers))

#Dictionary( Key - Value Pair)
student = {"name": "atanu", "age": 35, "email": "atanu@gmail.com"}
print(type(student))

#Set( Unordered Collection of Unique Elements)
numbers = {1, 2, 3, 4, 5}
print(type(numbers))


res = None
print(type(res))

