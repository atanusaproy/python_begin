#Logical operators: and , or , not

#1 - and operator
a = 10
b = 20
print(True and True)
print((a>b) and (a<b))
print(False and True)
print(False and False)

#2 - or operator
print(True or True)
print(True or False)
print(False or True)
print(False or False)

#3 - not operator
print(not True)
print(not False)